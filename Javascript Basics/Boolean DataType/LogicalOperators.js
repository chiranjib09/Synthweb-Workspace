//  Logical operators allow you to combine or negate Boolean expressions.

//  && (AND): Returns true if both operands are true.
//  || (OR): Returns true if either operand is true.
//   (NOT): Inverts the Boolean value.

let x = true;
let y = false;

console.log(x && y);
console.log(x || y);
console.log(!x);
console.log(!y);
