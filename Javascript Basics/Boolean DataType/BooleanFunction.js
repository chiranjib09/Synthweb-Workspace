//JavaScript provides the Boolean() function to convert other data types into a Boolean value.

//Truthy and Falsy Values:
//In JavaScript, all values are inherently either "truthy" or "falsy."

//Falsy values: Values that are considered false when converted to Boolean.

//false, 0, "" (empty string), null, undefined, NaN
//Truthy values: All other values, including non-empty strings, non-zero numbers, objects, and arrays.


console.log(Boolean(0));
console.log(Boolean("Hello"));
console.log(Boolean(null));
console.log(Boolean([]));
