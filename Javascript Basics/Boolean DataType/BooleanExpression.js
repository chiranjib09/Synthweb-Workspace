//Expressions in JavaScript can evaluate to true or false using comparison operators.

//Comparison Operators:
//== (loose equality): Compares values for equality, performing type conversion if necessary.
//=== (strict equality): Compares values for equality without type conversion.
//  != (loose inequality): Checks if values are not equal, allowing type conversion.
//  !== (strict inequality): Checks if values are not equal without type conversion.
// >, >=, <, <=: Greater than, less than, etc.

console.log(5 == '5');
console.log(5 === '5');
console.log(10 > 3);
console.log(5 !== 5);
