
let randomNum = Math.random();

while (randomNum <= 0.8) {
  console.log(randomNum);
  randomNum = Math.random();
}

console.log("Finished, random number is greater than 0.8:", randomNum);


//The while loop evaluates the condition before each iteration.
//If the condition is true, the code block within the loop is executed.
// The loop repeats as long as the condition remains true.
